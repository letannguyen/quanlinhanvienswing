    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

        
        try {
            if (kT()) {
            NhanVien nv = new NhanVien();
        
            nv.setMaNV(txtMaNV.getText());
            nv.setHoTen(txtHoTen.getText());
            nv.setTuoi(Integer.parseInt(txtAge.getText()));
            nv.setEmail(txtEmail.getText());
            nv.setLuong(Float.parseFloat(txtLuong.getText()));

            if (index == -1) {
                for(NhanVien nv1 : nvls.nvl){
                    if(nv.maNV.equalsIgnoreCase(nv1.maNV)){
                        JOptionPane.showMessageDialog(this, "Mã nhân viên tồn tại","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                        txtMaNV.requestFocus();
                        return;
                    }
                }
                add(nv);
                btnNewActionPerformed(evt);
                loadTable();
                updateRecord();
                JOptionPane.showMessageDialog(rootPane, "Lưu thành công");
            }
            else{
                updateNV();
                loadTable();
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công");
            }
        }
        }catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, "Lỗi "+e.getMessage());
        }
        
        // TODO add your handling code here:
    }//GEN-LAST:event_btnSaveActionPerformed

