    //bảng
    public void loadTable(){
        model.setRowCount(0);
        Object rowData[] = new Object[5];
        for (int i = 0; i < nvls.nvl.size(); i++) {
            rowData[0] = nvls.nvl.get(i).maNV;
            rowData[1] = nvls.nvl.get(i).hoTen;
            rowData[2] = nvls.nvl.get(i).tuoi;
            rowData[3] = nvls.nvl.get(i).email;
            rowData[4] = nvls.nvl.get(i).luong;
            model.addRow(rowData);
        }
    }
    
