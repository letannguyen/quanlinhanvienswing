    //kiểm tra để trống
    public boolean kT(){
        if (txtMaNV.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập mã nhân viên","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtMaNV.requestFocus();
            return false;
        }

        if (txtHoTen.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập họ tên","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtHoTen.requestFocus();
            return false;
        }
        
        if (txtAge.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập tuổi","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtAge.requestFocus();
            return false;
         }
        int tuoi = Integer.parseInt(txtAge.getText());
        if (tuoi < 16 || tuoi > 55) {
            JOptionPane.showMessageDialog(this, "Tuổi phải từ 15 đến 55","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtAge.requestFocus();
            return false;
            }

        if (txtEmail.getText().matches("[a-zA-Z0-9]+[._a-zA-Z0-9!#$%&'*+-/=?^_`{|}~]*[a-zA-Z]*@[a-zA-Z0-9]{2,8}.[a-zA-Z.]{1,6}")) {
        } else {
            JOptionPane.showMessageDialog(this, "Email chưa đúng định dạng","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtEmail.requestFocus();
            return false;
        }

        if (txtLuong.getText().equals("")) {
            JOptionPane.showMessageDialog(this, "Chưa nhập vào lương","Thông báo",JOptionPane.INFORMATION_MESSAGE);
            txtLuong.requestFocus();
            return false;
        }
        double luong = Double.parseDouble(txtLuong.getText());
            if (luong < 5000000) {
                JOptionPane.showMessageDialog(this, "Lương phải lớn hơn 5tr","Thông báo",JOptionPane.INFORMATION_MESSAGE);
                txtLuong.requestFocus();
                return false;
            }
        return true;
    }
    
    
    
